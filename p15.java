class p15 {
    public static void main(String[] args) {
        int n=7;
        while(n>=1){
            if(n%2==0){
                System.out.println(n+"");
                n--;
            }else{
                System.out.println(n+"");
                n=n-2;
            };
        }System.out.println();
    }
}
